  `nickname` VARCHAR(20) NOT NULL PRIMARY KEY,
  `name` VARCHAR(100) NOT NULL,
  `gender` INTEGER NOT NULL,
  `age` INTEGER NOT NULL,
  `email` VARCHAR(100) NOT NULL,
